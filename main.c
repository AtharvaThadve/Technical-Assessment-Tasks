void Motor_SetSpeed(float control)
{
    if (control > 0)
    {
        HAL_GPIO_WritePin(DIR_GPIO_Port, DIR_Pin, GPIO_PIN_SET); // Forward
        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, (uint16_t)control);
    }
    else if (control < 0)
    {
        HAL_GPIO_WritePin(DIR_GPIO_Port, DIR_Pin, GPIO_PIN_RESET); // Reverse
        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, (uint16_t)(-control));
    }
    else
    {
        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 0); // Stop motor
    }
}

float Encoder_GetPosition(void)
{
    int32_t counts = __HAL_TIM_GET_COUNTER(&htim2);
    return (counts / (float)COUNTS_PER_REV) * LEADSCREW_PITCH;
}

float PID_Control(float setpoint, float current)
{
    float error = setpoint - current;
    integral += error;
    float derivative = error - prev_error;
    prev_error = error;

    float output = Kp * error + Ki * integral + Kd * derivative;

    // Limit output
    if (output > 999) output = 999;
    if (output < -999) output = -999;

    return output;
}




